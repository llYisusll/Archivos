    private void jButtonConvertirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConvertirActionPerformed
        // TODO add your handling code here:
        this.a = 0.0;
        this.b = 0.0;
        this.oper = 0.0;
        if(this.jRadioButtonFaC.isSelected()){
            this.fahr = this.jTextFieldFahrenheit.getText().trim();
            this.a = Double.parseDouble(fahr);
            this.oper = (this.a-32)*5/9;
            this.jTextFieldCentigrados.setText(String.format("%.3f", this.oper));
        }
        
        if(this.jRadioButtonCaF.isSelected()){
            this.cent = this.jTextFieldCentigrados.getText().trim();
            this.b = Double.parseDouble(cent);
            this.oper = (this.b*9/5)+32;
            this.jTextFieldFahrenheit.setText(String.format("%.3f", this.oper));
        }
        
        
    }//GEN-LAST:event_jButtonConvertirActionPerformed

